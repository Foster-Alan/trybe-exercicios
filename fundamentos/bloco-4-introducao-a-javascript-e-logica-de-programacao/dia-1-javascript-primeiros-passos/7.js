let nota = 100;
if (nota >= 90) {
    console.log(nota = 'A');
}
else if (nota >= 80) {
    console.log(nota = 'B');
}
else if (nota >= 70) {
    console.log(nota = 'C');
}
else if (nota >= 60) {
    console.log(nota = 'D');
}
else if (nota >= 50) {
    console.log('E');
}
else if (nota < 50) {
    console.log(nota = 'F');
}
else if (nota < 0) {
    console.log(nota = 'Erro');
}
else if (nota > 100) {
    console.log(nota = 'Error');
}